using System;

class FloatOrDouble
{
    static void Main()
    {
        double doubleValue = 34.567839023;
        float floatValue = 12.345f;
        float secondFloatValue = 8923.1234857f;
        float thirdFloatValue = 3456.091f;
        Console.WriteLine(doubleValue + "\n" + floatValue + "\n" + secondFloatValue + "\n" + thirdFloatValue);
    }
}

